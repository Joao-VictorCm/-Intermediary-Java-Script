//Exemplo de Loops com While


var numero = 100

function testandowhile(){

    while(numero >= 1){
        console.log(numero + " garrafas na parede, " + numero + " garrafas de cerveja")
        numero --
        console.log("pegue uma e passe de mao em mao " + numero+ " garrafas de cerveja")
    }
}

testandowhile()
